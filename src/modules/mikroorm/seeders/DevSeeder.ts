import { EntityManager } from '@mikro-orm/core';
import { Seeder } from '@mikro-orm/seeder';
import { UserRoles } from '../entities/UserRoles';
import { Users } from '../entities/Users';
import { Games } from '../entities/Games';
import { Zones } from '../entities/Zones';
import { Launches } from '../entities/Launches';

export class DevSeeder extends Seeder {
  async run(em: EntityManager): Promise<void> {
    em.create(Users, {
      email: 'user@user.ru',
      password: 'useruser',
      role: em.getReference(UserRoles, 1),
    });
    for (let i = 0; i < zones.length; i++) {
      const zone = zones[i];
      em.create(Zones, {
        id: +zone.id,
        alias: zone.alias,
        name: zone.name,
        zoneHardwareKey: zone.zone_hardware_key,
        zoneStableHardwareKey: zone.zone_stable_hardware_key,
        zoneRegistered: +zone.zone_registered,
        zoneDisabled: !!+zone.zone_disabled,
        lastConnectionTimestamp: new Date(zone.last_connection_timestamp),
        launchesCount: +zone.launches_count,
        lastPlayedGame: +zone.last_played_game,
        demoLaunchesCount: +zone.demo_launches_count,
        demoMode: +zone.demo_mode,
        hideOnAdminPanel: !!+zone.hide_on_admin_panel,
        user: em.getReference(Users, Math.random() > 0.5 ? 1 : 2),
      });
    }
    await em.flush();
    for (let i = 0; i < launches.length; i++) {
      const launch = launches[i];
      em.create(Launches, {
        id: +launch.id,
        zone: em.getReference(Zones, +launch.zone_id),
        game: em.getReference(Games, +launch.game_id),
        timestamp: new Date(launch.timestamp),
      });
    }
  }
}

